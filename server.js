// ── GAP 4: Persistent session memory ─────────────────────────────────────────
// chatSessions survive server restarts via a lightweight JSON sidecar file.
// Only session context (geo/quarter/OA/ut30) is persisted — not full chat history.
const SESSIONS_PATH = path.join(__dirname, "chat-sessions.json");
function loadSessions() {
  try {
    if (fs.existsSync(SESSIONS_PATH)) {
      return JSON.parse(fs.readFileSync(SESSIONS_PATH, "utf8"));
    }
  } catch (_) { /* corrupt file — start fresh */ }
  return {};
}
