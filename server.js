// ─── GAP 1: KPI Targets & RAG status ─────────────────────────────────────────
// Declarative target definitions per Offering Area and WW.
// Operators update these values each quarter; the engine computes RAG automatically.
// RAG: GREEN = within 5% of target, AMBER = -5% to -15%, RED = >-15% below target.
const KPI_TARGETS = {
  WW:                              { signingsYoY: 10, revenueYoY: 8, winRate: 55, pipelineCoverage: 2.5 },
  "Project Services":              { signingsYoY: 16, revenueYoY: 10, winRate: 55, pipelineCoverage: 2.5 },
  "Interoperability Services":     { signingsYoY: 20, revenueYoY: 12, winRate: 60, pipelineCoverage: 3.0 },
  "Custom Services":               { signingsYoY: 8,  revenueYoY: 6,  winRate: 50, pipelineCoverage: 2.0 },
  "Other Services":                { signingsYoY: 5,  revenueYoY: 5,  winRate: 50, pipelineCoverage: 2.0 },
};

function ragStatus(actual, target) {
  if (target == null || actual == null) return "GRAY";
  const delta = actual - target;
  if (delta >= -target * 0.05) return "GREEN";
  if (delta >= -target * 0.15) return "AMBER";
  return "RED";
}

// GET /api/kpi/targets — returns targets + actuals + RAG status for the current filter context
app.get("/api/kpi/targets", (req, res) => {
  const { geo = "WW", quarter = "2Q26", offeringArea = "ALL" } = req.query;

  const CQ   = quarter !== "ALL" ? quarter : currentQuarter();
  const qNum = parseInt(CQ[0]), qYr = parseInt(CQ.slice(2));
  const PYQ  = `${qNum}Q${qYr - 1}`;

  const base = { geo, quarter: CQ };
  if (offeringArea !== "ALL") base.offeringArea = offeringArea;

  const recs  = filter(base);
  const recsY = filter({ ...base, quarter: PYQ });
  const closed  = recs.filter(r => r.stage === "Won" || r.stage === "Lost");
  const won     = closed.filter(r => r.stage === "Won");
  const active  = recs.filter(r => !["Won","Lost"].includes(r.stage));

  const sigCQ  = recs.reduce((s, r) => s + r.wonDollar, 0);
  const sigPYQ = recsY.reduce((s, r) => s + r.wonDollar, 0);
  const sigYoY = sigPYQ > 0 ? +((sigCQ / sigPYQ - 1) * 100).toFixed(1) : null;

  const revCQ  = filterRev({ geo, quarter: CQ,  offeringArea: offeringArea !== "ALL" ? offeringArea : undefined }).reduce((s,r)=>s+r.revActM,0);
  const revPYQ = filterRev({ geo, quarter: PYQ, offeringArea: offeringArea !== "ALL" ? offeringArea : undefined }).reduce((s,r)=>s+r.revActM,0);
  const revYoY = revPYQ > 0 ? +((revCQ / revPYQ - 1) * 100).toFixed(1) : null;

  const winRateActual = closed.length > 0 ? +((won.length / closed.length) * 100).toFixed(1) : null;
  const pipe = active.reduce((s, r) => s + r.oppVal, 0);
  const coverage = sigCQ > 0 ? +(pipe / sigCQ).toFixed(2) : null;

  const tKey  = offeringArea !== "ALL" ? offeringArea : "WW";
  const tgt   = KPI_TARGETS[tKey] || KPI_TARGETS.WW;

  const result = {
    scope: { geo, quarter: CQ, offeringArea },
    targets: tgt,
    actuals: {
      signingsYoY:      sigYoY,
      revenueYoY:       revYoY,
      winRate:          winRateActual,
      pipelineCoverage: coverage,
    },
    rag: {
      signingsYoY:      ragStatus(sigYoY, tgt.signingsYoY),
      revenueYoY:       ragStatus(revYoY, tgt.revenueYoY),
      winRate:          winRateActual != null ? (winRateActual >= tgt.winRate ? "GREEN" : winRateActual >= tgt.winRate * 0.9 ? "AMBER" : "RED") : "GRAY",
      pipelineCoverage: coverage != null ? (coverage >= tgt.pipelineCoverage ? "GREEN" : coverage >= tgt.pipelineCoverage * 0.8 ? "AMBER" : "RED") : "GRAY",
    },
  };
  res.json(result);
});

